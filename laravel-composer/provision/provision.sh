#!/bin/bash

# ------------------------------------------------------------------------------
# PHP CLI
# ------------------------------------------------------------------------------

cp /provision/php.fpm.ini /etc/php5/fpm/php.ini

php5enmod mcrypt

# ------------------------------------------------------------------------------
# Node build tools
# ------------------------------------------------------------------------------

npm install -g grunt-cli
